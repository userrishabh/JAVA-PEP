package assignment1;

public class Labels_break_Continue {

	public static void main(String[] args) {
		Task_for_continue:
		      for(int i=0; i<10; i++){
		          if (i<5){
		             continue Task_for_continue;
		          }
		          System.out.println("......."+i );
		       }
		System.out.println();
		
		// label with break
		Task_for_break:
		 for(int i=0; i<10; i++){
	         if (i==8){
	            break Task_for_break;
	          }
	          System.out.println("......."+i );
		 }

	}

}
